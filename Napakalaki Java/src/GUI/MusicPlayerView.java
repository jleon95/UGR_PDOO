    public void reproducirAudio(ArrayList<String> archivos) {
        
        Files = archivos;
        player = new MusicPlayer(archivos);
        player.start();
    }
    
    public void cerrarAudio() {
        
        player.Stop();
        try { player.join(); }
        catch (Exception e) {}
    }
    
